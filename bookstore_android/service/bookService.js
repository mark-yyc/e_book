import {apiUrl} from "../urlConfig";

const GET_BOOKS_URL = apiUrl + "/getBooks";

export function getBooks(data, callback) {
    let opts = {
        method: "POST",
        body: JSON.stringify(data),
        headers: {
            'Content-Type': 'application/json'
        },
        credentials: "include"
    };

    fetch(GET_BOOKS_URL, opts)
        .then((response) => {
            return response.json()
        })
        .then((data) => {
            callback(data);
        })
        .catch((error) => {
            console.log(error);
        });
}